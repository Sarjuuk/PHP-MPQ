class Huffman
{
    public static $PROBABILITIY_TABLES;

    private $nodes = null;
    private $sorted2;

    private $root = null;

    private $bitBuffer;
    private $bitNumber;
    private $source;

    private function setSource($source)
    {
        $this->sorted2 = TreeMap::create();
        $this->source = $source;
        $this->bitBuffer = 0;
        $this->bitNumber = 0;
    }

    private function getBits($bits)
    {
        while($this->bitNumber < $bits && $this->source->canRead())
        {
            $value = $this->source->get();

            $this->bitBuffer |= ((int)$value & 0xFF) << $this->bitNumber;

            $this->bitNumber += 8;
        }

        $result = $this->bitBuffer & ((1 << $bits) - 1);

        $this->bitBuffer = unsignedRightShift($this->bitBuffer, $bits);
        $this->bitNumber -= $bits;

        return $result;
    }

    private function getNode() {
        $node = null;
        if ($this->nodes === null) 
        {
            $node = new HuffmanNode();
        }
        else 
        {
            $node = $this->nodes;
            $this->nodes = $this->nodes->removeFromList();
        }
        return $node;
    }

    private function destroyTree($root) {
        if ($this->nodes === null) $this->nodes = root;
        else $this->nodes->joinList($root);
        $this->root = null;
        $this->sorted2->clear();
    }

    private function insertNode($node) {
        try{
            $test2 = $this->sorted2->ceiling($node->probability);
        }
        catch(\OutOfBoundsException $e){
            $test2 = null;
        }

        $current = null;

        if ($test2 !== null) 
        {
            $current = $test2->value;
            $node->insertAfter($current);
        } 
        else 
        {
            if ($this->root !== null) 
            {
                $node->insertAfter($this->root->prev);
            }
            else 
            {
                $node->newList();
            }
            $this->root = $node;
        }

        $this->sorted2->put([$node->probability => $node]);
    }

   private function addValueToTree($value) 
   {
        // create leaf node
        $node = $this->getNode();
        $node->value = $value;
        $node->probability = 0;
        $node->child[0] = null;
        $node->child[1] = null;

        $this->insertNode($node);

        // create branch node
        $node2 = $this->getNode();
        $child1 = $this->root->prev;
        $child2 = $child1->prev;

        $node2->value = -1;
        $node2->probability = $child1->probability + $child2->probability;
        $node2->child[0] = $child1;
        $node2->child[1] = $child2;
        $node2->parent = $child2->parent;

        $node2->insertAfter($child2->prev);

        // insert into tree
        if ($node2->parent->child[0] === $child2) $node2->parent->child[0] = $node2;
        else $node2->parent->child[1] = $node2;

        $child1->parent = $node2;
        $child2->parent = $node2;

        return $node;
    }

    private function incrementProbability($node) 
    {
        while ($node !== null)
        {
            // possible optimization here. Is all this really nescescary to enforce order?
            if ($this->sorted2->find($node->probability) === $node) 
            {
                if ($node->probability === $node->prev->probability)
                {
                    $this->sorted2->put([$node->probability => $node->prev]);
                }
                else
                {
                    $this->sorted2->remove($node->probability);
                }
            }

            $node->probability += 1;

            try{
                $test2 = $this->sorted2->ceiling($node->probability);
            }
            catch(\OutOfBoundsException $e){
                $test2 = null;
            }

            $where = null;

            if ($test2 !== null) 
                $where = $test2->value->next;
            else 
                $where = $this->root;

            if ($where !== $node && $where->parent !== null) 
            {      
                $node->listSwap($where);
                $node->treeSwap($where);

                if ($where->probability != $where->next->probability) 
                    $this->sorted2->put([$where->probability=>$where]);
            }

            $this->sorted2->put([$node->probability=>$node]);

            $node = $node->parent;
        }
    }

    public function buildTree($tree) 
    {
        $probabilities = Huffman::$PROBABILITIY_TABLES[$tree];

        // destroy any existing tree
        if ($this->root !== null) $this->destroyTree($root);

        // generate leaves
        for ($i = 0; $i < 0x102; $i++) 
        {
            $prob = (int)$probabilities[$i] & 0xFF;

            if ($prob == 0) continue;

            $node = $this->getNode();
            $node->value = $i;
            $node->probability = $prob;
            $node->child[0] = null;
            $node->child[1] = null;

            $this->insertNode($node);
        }

        // generate tree
        $current = $this->root->prev;
        while ($current !== $this->root) 
        {
            $node = $this->getNode();
            $child1 = $current;
            $child2 = $current = $current->prev;

            $child1->parent = $node;
            $child2->parent = $node;

            $node->value = -1;
            $node->probability = $child1->probability + $child2->probability;
            $node->child[0] = $child1;
            $node->child[1] = $child2;
            $this->insertNode($node);

            $current = $current->prev;
        }

        $this->root->parent = null;
    }

    public function decompress($in) 
    {
        $this->setSource(new ByteBuffer($in));
        $type = $this->getBits(8);
        $this->buildTree($type);

        $adjustProbability = $type == 0;

        $output = '';

        for (; ; ) 
        {
            $current = $this->root;

            while ($current->value == -1 && $this->source->canRead())
            {
                $current = $current->child[$this->getBits(1)];
            }

            if (!$this->source->canRead())
                break;

            if ($current->value == 0x101) 
            {
                $value = $this->getBits(8);
                $current = $this->addValueToTree($value);
                $this->incrementProbability($current);
                if (!$adjustProbability) $this->incrementProbability($current);
            } 
            else if ($current->value == 0x100) 
            {
                break;
            }

            $output .= pack("c", $current->value);

            if ($adjustProbability)
                $this->incrementProbability($current);
        }

        return $output;
    }
}

function unsignedRightShift($a, $b) 
{
    if ($b >= 32 || $b < -32) {
        $m = (int)($b/32);
        $b = $b-($m*32);
    }
    if ($b < 0) {
        $b = 32 + $b;
    }
    if ($b == 0) {
        return (($a>>1)&0x7fffffff)*2+(($a>>$b)&1);
    }
    if ($a < 0) 
    { 
        $a = ($a >> 1); 
        $a &= 0x7fffffff; 
        $a |= 0x40000000; 
        $a = ($a >> ($b - 1)); 
    } else { 
        $a = ($a >> $b); 
    }
    return $a; 
}

